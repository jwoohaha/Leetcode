class MyQueue:
    # stack 2개를 사용하여 queue 구현
    def __init__(self):
        self.input = []
        self.output = []

    def push(self, x: int) -> None:
        self.input.append(x)

    def pop(self) -> int:
        self.peek()
        return self.output.pop()

    def peek(self) -> int:
        # output이 없으면 input stack에서 output stack으로 입력
        # 한 번 더 스택을 거쳐 LIFO에서 FIFO으로 변환
        if not self.output:
            while self.input:
                self.output.append(self.input.pop())
        return self.output[-1]

    def empty(self) -> bool:
        return self.input == [] and self.output == []


